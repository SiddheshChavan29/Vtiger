class Shortconversion
{
 public static void main(String[]args) 
  {
	short s = 50 ;
	int i = s ;
	long l = s ;
	float f = s ;
	double d = s ;
	char c = s ;
    

    System.out.println(i) ;
	System.out.println(l) ;
	System.out.println(f) ;
	System.out.println(d) ;
	System.out.println(c) ;
  }
}
  
